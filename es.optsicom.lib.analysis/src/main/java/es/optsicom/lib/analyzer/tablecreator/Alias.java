package es.optsicom.lib.analyzer.tablecreator;

public class Alias {

	private String from;
	private String to;

	public Alias(String from, String to) {
		this.from = from;
		this.to = to;
	}

	public String getFrom() {
		return from;
	}

	public String getTo() {
		return to;
	}

}
